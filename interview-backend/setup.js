function createDirectories() {
  const directories = [
    'uploads',
    'uploads/audio',
    'temp_audio',
    'logs'
  ];

  directories.forEach(dir => {
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
      console.log(`ðŸ“ Created directory: ${dir}`);
    }
  });
}

