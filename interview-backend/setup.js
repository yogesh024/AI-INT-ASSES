async function main() {
  try {
    const setupConfig = await setupConfiguration();
    
    console.log('\nðŸ“ Generating configuration files...');
    
    // Generate .env file
    const envContent = generateEnvFile(setupConfig);
    fs.writeFileSync('.env', envContent);
    console.log('âœ… Generated .env file');
    
    // Create necessary directories
    createDirectories();
    
    // Show summary
    showSummary(setupConfig);
    
    // Show next steps
    showNextSteps();
    
    console.log('\nðŸŽ‰ Setup completed successfully!');
    console.log('Your AI Interview platform is ready to run.');
    
  } catch (error) {
    console.error('âŒ Setup failed:', error);
    process.exit(1);
  } finally {
    rl.close();
  }
}

// Check if config files already exist
if (fs.existsSync('.env')) {
  console.log('âš ï¸ Warning: .env file already exists');
  rl.question('Do you want to overwrite it? (y/n): ', (answer) => {
    if (answer.toLowerCase() === 'y') {
      main();
    } else {
      console.log('Setup cancelled. Your existing configuration is preserved.');
      rl.close();
    }
  });
} else {
  main();
}

// Handle process termination
process.on('SIGINT', () => {
  console.log('\n\nðŸ‘‹ Setup interrupted. Your configuration may be incomplete.');
  rl.close();
  process.exit(0);
});